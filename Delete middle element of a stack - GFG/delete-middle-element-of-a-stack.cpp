class Solution
{
    public:
    //Function to delete middle element of a stack.
    void deleteMid(stack<int>&s, int sizeOfStack)
    {
        // code here.. 
        stack<int> st;
        while(!s.empty())
        {
            st.push(s.top());
            s.pop();
        }
        
        int index = ceil((sizeOfStack + 1)/2);
        int count = 1;
        while(!st.empty()){
            if( count == index) st.pop();
            
            else{
                s.push(st.top());
                st.pop();
            }
            
            count++;
        }
    }
};

